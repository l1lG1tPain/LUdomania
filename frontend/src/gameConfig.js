// ==========================================

export function getPrizeVisual(prizeId) {
    const prize = PRIZES[prizeId];
    if (!prize) {
        return {
            type: "unknown",
            isNft: false,
            emoji: "❓",
            src: null,
            alt: prizeId,
        };
    }

    if (prize.type === "nft" && prize.imageUrl) {
        return {
            type: "nft",
            isNft: true,
            emoji: null,
            src: prize.imageUrl,
            alt: prize.name || prizeId,
        };
    }

    return {
        type: prize.type || "regular",
        isNft: false,
        emoji: prize.emoji || "❓",
        src: null,
        alt: prize.name || prizeId,
    };
}

